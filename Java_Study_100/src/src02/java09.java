package src02;

public class java09 {

	public static void main(String[] args) {
		// [ ! ] : 축약된 형태
		int a=0, b=100; 
		
		a = a + 1;		
		System.out.println( a );  // 1
		
		a += 1;
		System.out.println( a );  // 2
		
		a -= 1;
		System.out.println( a );  // 1
		
		b++;
		System.out.println( b );  // 101
		
		b++;
		System.out.println( b );  // 102
		
		b--;
		System.out.println( b );  // 101

	}

}
